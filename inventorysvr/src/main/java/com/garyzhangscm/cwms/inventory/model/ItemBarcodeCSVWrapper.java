package com.garyzhangscm.cwms.inventory.model;


import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import org.apache.logging.log4j.util.Strings;

import java.io.Serializable;

public class ItemBarcodeCSVWrapper implements Serializable {
    // item name
    private String name;

    private String client;
    private String code;
    private String type;

    public ItemBarcodeCSVWrapper trim() {

        name = Strings.isBlank(name) ? "" : name.trim();

        code = Strings.isBlank(code) ? "" : code.trim();

        client = Strings.isBlank(client) ? "" : client.trim();
        type = Strings.isBlank(type) ? "" : type.trim();


        return this;
    }

    @Override
    public String toString() {
        try {
            return new ObjectMapper().writerWithDefaultPrettyPrinter().writeValueAsString(this);
        } catch (JsonProcessingException e) {
            e.printStackTrace();
        }
        return null;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClient() {
        return client;
    }

    public void setClient(String client) {
        this.client = client;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
