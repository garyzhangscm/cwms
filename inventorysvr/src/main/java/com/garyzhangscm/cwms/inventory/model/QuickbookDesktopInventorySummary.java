package com.garyzhangscm.cwms.inventory.model;


import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;

import java.io.Serializable;

public class QuickbookDesktopInventorySummary  implements Serializable {

     private String itemName;
     private String listId;
     private String inventoryStatus;
     private Long quantity;

    public QuickbookDesktopInventorySummary(){

    }
    public QuickbookDesktopInventorySummary(String itemName, String listId, String inventoryStatus, Long quantity) {
        this.itemName = itemName;
        this.listId = listId;
        this.inventoryStatus = inventoryStatus;
        this.quantity = quantity;
    }

    @Override
    public String toString() {
        try {
            return new ObjectMapper().writerWithDefaultPrettyPrinter().writeValueAsString(this);
        } catch (JsonProcessingException e) {
            e.printStackTrace();
        }
        return null;
    }


    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getListId() {
        return listId;
    }

    public void setListId(String listId) {
        this.listId = listId;
    }

    public String getInventoryStatus() {
        return inventoryStatus;
    }

    public void setInventoryStatus(String inventoryStatus) {
        this.inventoryStatus = inventoryStatus;
    }

    public Long getQuantity() {
        return quantity;
    }

    public void setQuantity(Long quantity) {
        this.quantity = quantity;
    }
}
